/** Define a test image */
#define IMAGE( NAME, DATA )						\
	static const uint8_t NAME ## _data[] = DATA;			\
	static struct cms_test_image NAME = {				\
		.image = {						\
			.refcnt = REF_INIT ( ref_no_free ),		\
			.name = #NAME,					\
			.flags = ( IMAGE_STATIC | IMAGE_STATIC_NAME ),	\
			.data = NAME ## _data,				\
			.len = sizeof ( NAME ## _data ),		\
		},							\
	}

